package polymorphism;

public class LgTV {
	public void turnOn() {
		System.out.println("엘지 티비 전원 켠다.");	
	}
	
	public void turnOff() {
		System.out.println("엘지 티비 전원 끈다.");
	}
	
	public void soundUp() {
		System.out.println("엘지 티비 소리 높인다.");
	}
	
	public void soundDown() {
		System.out.println("엘지 티비 소리 내린다.");
	}
}
